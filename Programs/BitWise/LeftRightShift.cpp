#include<bits/stdc++.h>
using namespace std;

int main()
{

    // Left Shift............
    cout<<(1<<2)<<"\n";
    cout<<(1<<4)<<"\n";
    cout<<(2<<4)<<"\n";

    // Right Shift ...................................
    cout<<(4>>1)<<"\n";
    cout<<(8>>2)<<"\n";
    cout<<(6>>1)<<"\n";

    return 0;
}
